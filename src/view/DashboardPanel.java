    private void btnGENERATE1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGENERATE1ActionPerformed

        try {
        // 1. Get dates from JDateChoosers
        java.util.Date fromDate = fromDateChooser.getDate();
        java.util.Date toDate = toDateChooser.getDate();

        // 2. Validate dates (optional but recommended)
        if (fromDate == null || toDate == null) {
            SelectDateValidation sdv=new SelectDateValidation();
            sdv.setVisible(true);
            return;
        }

        // 3. Put dates into parameters map
        Map<String, Object> parameters = new HashMap<>();
        parameters.put("fromDate", fromDate);
        parameters.put("toDate", toDate);

        // 4. Compile and fill the report
        String reportPath = "D:\\A\\HardwareSYS\\src\\Reports\\SalesDistributionReport.jrxml";
        JasperReport jr = JasperCompileManager.compileReport(reportPath);
        JasperPrint jp = JasperFillManager.fillReport(jr, parameters, DBConnection.createDBConnection());

        // 5. View the report
        JasperViewer viewer = new JasperViewer(jp, false);
            viewer.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
            viewer.setVisible(true);
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnGENERATE1ActionPerformed

