    public void LQS_Load() {
    try {
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT  Item_id, Iname, Supplier, Quantity FROM items WHERE Quantity < 10");

        ResultSetMetaData rsd = rs.getMetaData();
        int columnCount = rsd.getColumnCount();

        DefaultTableModel model = (DefaultTableModel) tblLQS.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            Vector<String> row = new Vector<>(columnCount);
            row.add(rs.getString("Item_id"));
            row.add(rs.getString("Iname"));
            row.add(rs.getString("Supplier"));
            row.add(rs.getString("Quantity"));
            
            model.addRow(row);
        }
    } catch (SQLException ex) {
        DBErrorMessage dbem=new DBErrorMessage();
        dbem.setVisible(true);
    }
}

